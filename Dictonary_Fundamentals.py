d = {'a':10 , 'b': 20 , 'c': 30 , 'd': 40 , 'e': 50}
print(d) # {'a': 10, 'b': 20, 'c': 30, 'd': 40, 'e': 50}
print(d.keys()) # dict_keys(['a', 'b', 'c', 'd', 'e'])
print(type(d.keys()))
print(d.values()) # dict_values([10, 20, 30, 40, 50])
print(type(d.values()))
print(d.items()) # dict_items([('a', 10), ('b', 20), ('c', 30), ('d', 40), ('e', 50)])
print(type(d.items()))



